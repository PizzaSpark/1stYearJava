public class arithmeticEquations {

	public static void main(String[] args) {

		int x = 17;
		int y = 12;

		int sum = x + y;
		int difference = x - y;
		int product = x * y;
		int quotient = x / y;

		System.out.printf("%d + %d = %d", x, y, sum);
		System.out.println();
		System.out.printf("%d - %d = %d", x, y, difference);
		System.out.println();
		System.out.printf("%d * %d = %d", x, y, product);
		System.out.println();
		System.out.printf("%d / %d = %d", x, y, quotient);
		System.out.println();

	}
}
